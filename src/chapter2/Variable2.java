package chapter2;

// 변수 : 기억장소를 가리키는 이름.
/*
 자바스크립트 : 변수를 선언하는 키워드. var, let, const
 */
public class Variable2 {

	public static void main(String[] args) {
		// 변수를 선언
		// 데이터타입 : byte, short, int, long
		// 문법) 데이터타입 변수;
		// 변수이름은 한글도 지원하지만 권장안함.
		int level;
		level = 10;
		
		System.out.println(level);
		
	}
}
